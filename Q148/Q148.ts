// Demonstrate  the use of the setTimeout() function to execute code after delay.
// setTimeout() is a built in javascript function that allows you to execute a piece of code after a specified delay.it is especially useful for scheduling tasks,animations or delaying execution without blocking the main thread.




// uses setTimeout() to log a message after a 2-second delay

setTimeout(()=>{
    console.log("This message is shown after a 2-second delay");
},2000);

